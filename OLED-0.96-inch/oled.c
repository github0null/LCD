#define CHAR_8X16(_code) _ascii_8x16[(_code)-32]

void OLED_WriteString_8x16(const char *_str)
{
    uint16_t index = 0;

    while (_str[index] != 0)
    {
        if (_str[index] < 32)
            OLED_WriteData_8x16(CHAR_8X16(32));
        else if (_str[index] > 126)
            OLED_WriteData_8x16(CHAR_8X16('?'));
        else
            OLED_WriteData_8x16(CHAR_8X16(_str[index]));

        index++;
    }
}

